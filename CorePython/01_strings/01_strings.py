# Python supports both procedural and object oriented programming approach.

# String: String is just a sequence of characters.
# Python has multiple ways of creating a string
# Strings are immutable in python.

message1 = "aditya pawar" # String inside double quotes
message2 = '"aditya pawar"' # String inside single quotes
message3 = "hello 'aditya'"
message4 = f"Hello {message1}" # f string - format string - supports string interpolation.
message5 = '''
Multi
line
string
'''

print(message1, message2, message3, message4, message5, sep="\n")